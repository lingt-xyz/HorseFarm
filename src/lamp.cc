Lamp::Lamp(Shader& shader){

    this->shader_ = shader;

    GLuint VBO;

    glGenVertexArrays(1, &this->quadVAO_);
    glGenBuffers(1, &VBO);

    glBindVertexArray(this->quadVAO_);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 7 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(2);
    glVertexAttribPointer(2, 4, GL_FLOAT, GL_FALSE, 7 * sizeof(float), (void*)(3*sizeof(float)));

    glBindVertexArray(0);
}

Lamp::~Lamp(){
}

void Lamp::Draw(){
    this->shader_.Use();
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, light_pos);
    model = glm::scale(model, glm::vec3(0.2f)); // a smaller cube
    this->shader_.SetMatrix4("model", model);

    glBindVertexArray(this->quadVAO_);

    glDrawArrays(GL_TRIANGLES, 0, 36);

    glBindVertexArray(0);
}
