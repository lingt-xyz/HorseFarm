Horse::Horse()
{
    //color = glm::vec4(getRandomFloat(), getRandomFloat(), getRandomFloat(), 1.0f);

    glGenVertexArrays(1, &this->quadVAO_);
    glBindVertexArray(quadVAO_);
    unsigned int VBO;
    glGenBuffers(1, &VBO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // normal attribute
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
    // texture
    glEnableVertexAttribArray(2);
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
    glBindVertexArray(0);
}

Horse::~Horse()
{
}

void Horse::Draw(Shader& shader)
{
    this->shader_ = shader;
    this->shader_.Use();

    if(Controller::texture_on && !Controller::shadow_on){
        this->shader_.SetInteger("material.diffuse", 0);
        this->shader_.SetVector3f("material.specular", glm::vec3(0.5f, 0.5f, 0.5f));
        this->shader_.SetFloat("material.shininess", 64.0f);
    }

    glBindVertexArray(this->quadVAO_);

    if(run_on) // let's run
    {
        run();
        ++tmp_time;
        tmp_time %= (speedDivision+1);
        tmp_step += (tmp_time / speedDivision);
        if(tmp_step > 6)
        {
            tmp_step = 1;
        }
    }

    this->BuildModel();

    glBindVertexArray(0);
}
